  import { initScrollAnimations } from './ui.js';

  function renderTeamCards() {
    const container = document.getElementById('team-container');
    if (!container) return;
    
    teamData.forEach(member => {
      const card = document.createElement('div');
      card.className = 'team-card animate-on-scroll';
      
      card.innerHTML = `
        <div class="team-img">
          <img src="${member.image}" alt="${member.role}" loading="lazy">
        </div>
        <div class="team-info">
          <h3>${member.name}</h3>
          <p>${member.role}</p>
          <div class="team-social">
            <a href="${member.social.instagram}" class="social-icon" aria-label="Instagram ${member.name}">
              <i class="fab fa-instagram"></i>
            </a>
            <a href="${member.social.github}" class="social-icon" aria-label="GitHub ${member.name}">
              <i class="fab fa-github"></i>
            </a>
            <a href="${member.social.linkedin}" class="social-icon" aria-label="LinkedIn ${member.name}">
              <i class="fab fa-linkedin"></i>
            </a>
          </div>
        </div>
      `;
      
      container.appendChild(card);
    });
  }
  
// tim.js - Perbaiki inisialisasi
export function initTim() {
  // Pastikan container ada sebelum render
  if (document.getElementById('team-container')) {
    renderTeamCards();
    initScrollAnimations(); // Panggil ulang untuk elemen baru
  } else {
    setTimeout(initTim, 100); // Coba lagi setelah DOM siap
  }
}